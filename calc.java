
public class calc {
    public static void main(String[] args) {
        int a = Integer.parseInt(args[0]);
        int b = Integer.parseInt(args[1]);
        System.out.println("ADD: " + (a+b));
        System.out.println("SUB: " + (a-b));
        System.out.println("MUL: " + (a*b));
        System.out.println("DIV: " + (a/b));
    }
}
